#!/usr/bin/perl

use strict;
use warnings;

my $letters = "abcdefghijklmnopqrstuvwxyz_}";
my @to_replace = (0x41, 0x42, 0x43, 0x44, 0x45, 0x46, 0x47, 0x48, 0x49, 0x4a, 0x4b, 0x4c);
my @buff = (0x0f, 0x13, 0x0a, 0x0c, 0x06, 0x03, 0x07, 0x13, 0x07, 0xff - 0x100, 0xfa - 0x100, 0x10);

for (my $i = 0; $i < 0xc; $i++) {
	print substr($letters, $to_replace[$i] % 0x41 + $buff[$i], 1);
}
print "\n";
